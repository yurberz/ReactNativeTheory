interface IPoint2D {
  x: number;
  y: number;
  reset(): void;
}

interface IPoint3D {
  z: number;
  reset(): void;
}

class Point2D implements IPoint2D {
  public x = 0;
  public y = 0;

  constructor(x: number, y: number) {
    this.x = x;
    this.y = y;
  }

  public reset() {
    this.x = 0;
    this.y = 0;
  }
}

class Point3D extends Point2D implements IPoint2D {
  public z = 0;

  constructor(x: number, y: number, z: number) {
    super(x, y);

    this.z = z;
  }

  public reset() {
    super.reset();

    this.z = 0;
  }
}

const point3D = new Point3D(20, 30, 56);

point3D.reset();

